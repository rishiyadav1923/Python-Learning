# Python cmath.cosh() Method

# Example
# Find the hyperbolic cosine of a complex number:

#import cmath for complex number operations
import cmath

#find the hyperbolic cosine of a complex number
print (cmath.cosh(2 + 3j))

# Definition and Usage
# The cmath.cosh() method returns the hyperbolic cosine of a complex number.

# Syntax
# cmath.cosh(x)

# Parameter Values
# Parameter	                Description
# x	                        Required. A number to find the hyperbolic cosine of

# Technical Details
# Return Value:	A complex value, representing the hyperbolic cosine of a complex number
# Python Version:	1.5

