# Python cmath.sinh() Method

# Example
# Find the hyperbolic sine of a complex number:

#importing cmath for complex number operations
import cmath

#find the hyperbolic sine of a complex number
print (cmath.sinh(2 + 3j))

# Definition and Usage
# The cmath.sinh() method returns the hyperbolic sine of a complex number.

# Syntax
# cmath.sinh(x)

# Parameter Values
# Parameter	                Description
# x	                        Required. A number to find the hyperbolic sine of

# Technical Details
# Return Value:	A complex value, representing the hyperbolic sine of a complex number
# Python Version:	1.5

