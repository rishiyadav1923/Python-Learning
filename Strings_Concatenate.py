# Python - String Concatenation
# String Concatenation

# To concatenate, or combine, two strings you can use the + operator.
# Example

# Merge variable a with variable b into variable c:
a = "Hello"
b = "World"
c = a + b
print(c)

# Example

# To add a space between them, add a " ":
p = "Hello"
q = "World"
r = p + " " + q
print(r)

