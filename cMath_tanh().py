# Python cmath.tanh() Method

# Example
# Find the hyperbolic tangent of a complex number:

#Import cmath Library
import cmath

#Return the hyperbolic tangent of a complex number
print (cmath.tanh(2 + 3j))

# Definition and Usage
# The cmath.tanh() method returns the hyperbolic tangent of a complex number.

# Syntax
# cmath.tanh(x)

# Parameter Values
# Parameter	                    Description
# x	                            Required. A number to find the hyperbolic tangent of. 
#                               If the value is not a number, it returns a TypeError

# Technical Details
# Return Value:	A complex value, representing the hyperbolic tangent of a complex number
# Python Version:	1.5

