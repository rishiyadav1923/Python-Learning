# Python cmath.infj Constant

# Example
# Print complex infinity:

#Import cmath Library
import cmath

# Print complex infinity
print (cmath.infj)

# Definition and Usage
# The cmath.infj constant returns a complex positive infinity. 
# The return has a 0 real part, and positive infinity as the imaginary part.
# The cmath.infj constant is equivalent to complex(0.0, float("inf")).

# Syntax
# cmath.infj

# Technical Details
# Return Value:	A complex value, representing the value of complex infinity
# Python Version:	3.6

